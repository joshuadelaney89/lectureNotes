//Assignment operator
let x = 20;

//addition assignment operator
x += 1; //x = x + 1
console.log(x);

//subtraction assignment operator
x -= 1; //x = x - 1
console.log(x);

//multiplication
x *= 2; //x = x * 2
console.log(x);

//division
x /= 4; //x = x / 4
console.log(x);

//remainder (modulus) 
x %=2
console.log(x);

//modulus operator
console.log(5%3); //what's left over
console.log(25%7);

//exponential
x **= 2;
console.log(x);

let y = 7;
y**= 3;
console.log(y);